package com.zxtech.esp.vo.vrnewtestpaper;

public class TestPaperOperation {
	private Long id;

	private Long version;

	private Long milestoneId;

	private Long referenceMilestoneId;

	private Long referenceOperationId;

	private Long score;

	private Long sequenceNumber;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getVersion() {
		return version;
	}

	public void setVersion(Long version) {
		this.version = version;
	}

	public Long getMilestoneId() {
		return milestoneId;
	}

	public void setMilestoneId(Long milestoneId) {
		this.milestoneId = milestoneId;
	}

	public Long getReferenceMilestoneId() {
		return referenceMilestoneId;
	}

	public void setReferenceMilestoneId(Long referenceMilestoneId) {
		this.referenceMilestoneId = referenceMilestoneId;
	}

	public Long getReferenceOperationId() {
		return referenceOperationId;
	}

	public void setReferenceOperationId(Long referenceOperationId) {
		this.referenceOperationId = referenceOperationId;
	}

	public Long getScore() {
		return score;
	}

	public void setScore(Long score) {
		this.score = score;
	}

	public Long getSequenceNumber() {
		return sequenceNumber;
	}

	public void setSequenceNumber(Long sequenceNumber) {
		this.sequenceNumber = sequenceNumber;
	}

}